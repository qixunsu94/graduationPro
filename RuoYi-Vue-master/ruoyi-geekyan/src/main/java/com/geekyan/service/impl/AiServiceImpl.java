    private String callAiApi(JSONArray messages, String useModel) {
        if (apiKey == null || apiKey.isEmpty()) {
            log.error("AI API key is not configured");
            return null;
        }

        int maxRetries = 3;
        for (int attempt = 0; attempt <= maxRetries; attempt++) {
            JSONObject requestBody = new JSONObject();
            requestBody.put("model", useModel);
            requestBody.put("messages", messages);

            RequestBody body = RequestBody.create(
                    MediaType.parse("application/json; charset=utf-8"),
                    requestBody.toJSONString());

            Request request = new Request.Builder()
                    .url(apiUrl)
                    .addHeader("Authorization", "Bearer " + apiKey)
                    .addHeader("Content-Type", "application/json")
                    .post(body)
                    .build();

            try (Response response = httpClient.newCall(request).execute()) {
                if (response.isSuccessful() && response.body() != null) {
                    String responseBody = response.body().string();
                    JSONObject json = JSON.parseObject(responseBody);
                    JSONArray choices = json.getJSONArray("choices");
                    if (choices != null && !choices.isEmpty()) {
                        return choices.getJSONObject(0).getJSONObject("message").getString("content");
                    }
                    log.error("AI API response has no choices: {}", responseBody);
                    return null;
                }

                // 429速率限制：指数退避重试
                if (response.code() == 429 && attempt < maxRetries) {
                    long waitMs = (long) Math.pow(2, attempt) * 1000 + (long) (Math.random() * 500);
                    log.warn("AI API速率限制(429)，第{}次重试，等待{}ms", attempt + 1, waitMs);
                    try {
                        Thread.sleep(waitMs);
                    } catch (InterruptedException ie) {
                        Thread.currentThread().interrupt();
                        return null;
                    }
                    continue;
                }

                log.error("AI API call failed: code={}, body={}", response.code(),
                        response.body() != null ? response.body().string() : "empty");
                return null;
            } catch (IOException e) {
                if (attempt < maxRetries) {
                    long waitMs = (long) Math.pow(2, attempt) * 1000;
                    log.warn("AI API调用IO异常，第{}次重试: {}", attempt + 1, e.getMessage());
                    try {
                        Thread.sleep(waitMs);
                    } catch (InterruptedException ie) {
                        Thread.currentThread().interrupt();
                        return null;
                    }
                    continue;
                }
                log.error("AI API call error after {} retries", maxRetries, e);
                return null;
            }
        }
        return null;
    }
