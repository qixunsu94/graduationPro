    @Override
    public String translate(String text, String sourceLang, String targetLang) {
        // 优先从缓存获取
        String cached = queryCacheService.getTranslate(text, sourceLang, targetLang);
        if (cached != null) {
            log.debug("AI翻译缓存命中: text={}", text.substring(0, Math.min(text.length(), 20)));
            return cached;
        }

        JSONArray messages = new JSONArray();
        JSONObject systemMsg = new JSONObject();
        systemMsg.put("role", "system");
        systemMsg.put("content", "You are a professional translator. Translate the following text from " + sourceLang
                + " to " + targetLang + ". Only return the translation result.");
        messages.add(systemMsg);

        JSONObject userMsg = new JSONObject();
        userMsg.put("role", "user");
        userMsg.put("content", text);
        messages.add(userMsg);

        String result = callAiApi(messages);
        if (result != null && !result.isEmpty()) {
            queryCacheService.setTranslate(text, sourceLang, targetLang, result);
        }
        return result;
    }

